class WaterfallExhaustedError(Exception):
    pass


@retry(
    stop=stop_after_attempt(6),
    wait=wait_exponential(multiplier=2, min=4, max=60),
    retry=retry_if_exception_type(WaterfallExhaustedError),
    reraise=True,
)
async def fetch_openai_compat(
    client: AsyncOpenAI, prompt: str, temperature: float = 0.1
) -> tuple[str, str]:
    google_models = [
        "gemma-3-27b-it",
        "gemini-3.1-flash-lite-preview",
        "gemini-3-flash-preview",
        "gemini-2.5-flash",
    ]

    for model_name in google_models:
        try:
            kwargs = {
                "model": model_name,
                "messages": [{"role": "user", "content": prompt}],
                "temperature": temperature,
            }

            if "gemma" not in model_name:
                kwargs["response_format"] = {"type": "json_object"}

            response = await client.chat.completions.create(**kwargs)

            # The API returns None or empty string if it blocked the output for safety
            content = response.choices[0].message.content
            if not content:
                return "SAFETY_BLOCKED", model_name

            return content.strip(), model_name

        except Exception as e:
            error_str = str(e).lower()
            if any(code in error_str for code in ["429", "exhausted", "403", "503"]):
                continue  # Silently cascade down the waterfall
            else:
                print(f"      [!] Unexpected error with {model_name}: {e}")
                continue

    # If the loop exits normally, every model in the waterfall hit a rate limit.
    print("      [-] Waterfall exhausted. Triggering Tenacity backoff...")
    raise WaterfallExhaustedError("All models hit rate limits.")


async def safe_eval(
    client: AsyncOpenAI, prompt: str, semaphore: asyncio.Semaphore, identifier: str
) -> dict:
    async with semaphore:
        try:
            content, model = await fetch_openai_compat(client, prompt)
            if content == "SAFETY_BLOCKED":
                return {"status": "safety_blocked", "data": None}

            raw_content = (
                content.strip("`").replace("json\n", "", 1).strip()
                if content.startswith("```")
                else content
            )
            return {
                "status": "success",
                "data": json.loads(raw_content),
                "model": model,
            }
        except Exception as e:
            print(f"  [!] Eval failed for {identifier}: {e}")
            return {"status": "failed", "data": None}


def extract_milestones(records: list) -> list:
    """Extracts exactly 4 milestones from a list of GCG steps."""
    if not records:
        return []
    n = len(records)
    indices = [0, max(0, (n - 1) // 3), max(0, 2 * (n - 1) // 3), n - 1]
    return [records[i] for i in indices]


async def process_gcg_logs(
    jsonl_path: str, output_path: str, max_concurrency: int = 10
):
    client = AsyncOpenAI(
        api_key=os.getenv("GEMINI_API_KEY", "missing_key"),
        base_url="https://generativelanguage.googleapis.com/v1beta/openai/",
    )
    semaphore = asyncio.Semaphore(max_concurrency)

    print("[*] Loading Tokenizer for Mechanistic Mapping...")
    tokenizer = AutoTokenizer.from_pretrained(
        "deepseek-ai/DeepSeek-V3-0324", trust_remote_code=True
    )

    # 1. Group records by Sequence Length and Init Type
    batches = {}
    with open(jsonl_path, "r") as f:
        for line in f:
            rec = json.loads(line)
            # Grouping key: "Length 32 - Random Start"
            key = f"L{rec['sequence_length']}_{rec['init_type']}"
            if key not in batches:
                batches[key] = []
            batches[key].append(rec)

    final_report = {}

    # 2. Process each batch
    for batch_key, records in batches.items():
        print(f"\n[*] Analyzing Trajectory: {batch_key} ({len(records)} steps)")

        milestones = extract_milestones(records)

        # 1. Sanitize Strings (The Holistic View)
        strings = []
        for m in milestones:
            raw_str = m["decoded_string"]
            if raw_str.startswith("'") and raw_str.endswith("'"):
                raw_str = (
                    raw_str[1:-1]
                    .encode("latin1", "backslashreplace")
                    .decode("unicode_escape")
                )
            strings.append(raw_str)

        # 2. Build the Mapped Token Breakdown (The Mechanistic View)
        token_breakdowns = []
        for m in milestones:
            mapped_tokens = []
            for tid in m["token_ids"]:
                # Decode each token individually.
                # Using repr() ensures whitespace/newlines are visible to the LLM (e.g., ' arket' vs 'arket')
                token_text = repr(tokenizer.decode([tid]))
                mapped_tokens.append(f"[{tid}]: {token_text}")

            breakdown_str = ", ".join(mapped_tokens)
            token_breakdowns.append(breakdown_str)

        # Phase A: Concurrently analyze the 4 milestones
        print(f"  [-] Running Phase A (Milestone Analysis)...")
        phase_a_tasks = [
            safe_eval(
                client,
                PHASE_A_PROMPT.format(prompt_text=s, token_breakdown=tb),
                semaphore,
                f"{batch_key}_M{i}",
            )
            for i, (s, tb) in enumerate(zip(strings, token_breakdowns))
        ]
        phase_a_results = await asyncio.gather(*phase_a_tasks)

        # Phase B: Independent Trajectory Analysis
        print(f"  [-] Running Phase B (Trajectory Convergence)...")
        phase_b_prompt = PHASE_B_PROMPT.format(
            string_0=strings[0],
            string_1=strings[1],
            string_2=strings[2],
            string_3=strings[3],
        )
        phase_b_result = await safe_eval(
            client, phase_b_prompt, semaphore, f"{batch_key}_Trajectory"
        )

        # Compile
        final_report[batch_key] = {
            "milestone_strings": strings,
            "phase_a_milestones": [res.get("data") for res in phase_a_results],
            "phase_b_trajectory": phase_b_result.get("data"),
        }

    # 3. Save Report
    with open(output_path, "w") as f:
        json.dump(final_report, f, indent=2)
    print(f"\n[+] Analysis complete. Report saved to {output_path}")


