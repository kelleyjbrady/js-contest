import os
import json
import asyncio
from openai import AsyncOpenAI
from tenacity import (
    retry,
    stop_after_attempt,
    wait_exponential,
    retry_if_exception_type,
)

# ---------------------------------------------------------
# PROMPTS
# ---------------------------------------------------------
PHASE_A_PROMPT = """You are a forensic AI interpretability rater analyzing strings generated by an adversarial Greedy Coordinate Gradient (GCG) optimizer.
The optimizer frequently strips grammar and relies on associative latent-space geometry. 

Analyze this specific token string:
"{prompt_text}"

Return a JSON object with strictly these keys:
- "coherence_score": (int 1-10) How syntactically cohesive the string is.
- "main_concepts": (list of strings) Explicit concepts directly visible in the text.
- "associative_main_concepts": (list of strings) The underlying domain, execution structure, or semantic neighborhood these tokens mathematically point toward, even if fragmented.
"""

PHASE_B_PROMPT = """You are a forensic AI trajectory analyst. You are analyzing the evolution of a string as an adversarial optimizer forces it to converge on a specific region of a neural network's latent space.

Here is the chronological progression of the string:
1. Start (t=0): "{string_0}"
2. Early-Mid (t=33): "{string_1}"
3. Late-Mid (t=66): "{string_2}"
4. End (t=100): "{string_3}"

Return a JSON object with strictly these keys:
- "converging_domain": (string) The specific technical, structural, or semantic domain the optimizer is isolating.
- "abandoned_concepts": (list of strings) Concepts that were present early but purged.
- "surviving_concepts": (list of strings) The core load-bearing concepts that remained or were amplified by the end.
- "trajectory_analysis": (string) A 2-sentence summary of the optimization path.
"""


# ---------------------------------------------------------
# CORE LOGIC
# ---------------------------------------------------------
class WaterfallExhaustedError(Exception):
    pass


@retry(
    stop=stop_after_attempt(6),
    wait=wait_exponential(multiplier=2, min=4, max=60),
    retry=retry_if_exception_type(WaterfallExhaustedError),
    reraise=True,
)
async def fetch_openai_compat(
    client: AsyncOpenAI, prompt: str, temperature: float = 0.1
) -> tuple[str, str]:
    google_models = [
        "gemini-3.1-flash-lite-preview",
        "gemini-3-flash-preview",
        "gemini-2.5-flash",
    ]

    for model_name in google_models:
        try:
            response = await client.chat.completions.create(
                model=model_name,
                messages=[{"role": "user", "content": prompt}],
                temperature=temperature,
                response_format={"type": "json_object"},
            )
            content = response.choices[0].message.content
            if not content:
                return "SAFETY_BLOCKED", model_name
            return content.strip(), model_name
        except Exception as e:
            error_str = str(e).lower()
            if any(code in error_str for code in ["429", "exhausted", "403", "503"]):
                continue
            else:
                print(f"      [!] Unexpected error with {model_name}: {e}")
                continue

    raise WaterfallExhaustedError("All models hit rate limits.")


async def safe_eval(
    client: AsyncOpenAI, prompt: str, semaphore: asyncio.Semaphore, identifier: str
) -> dict:
    async with semaphore:
        try:
            content, model = await fetch_openai_compat(client, prompt)
            if content == "SAFETY_BLOCKED":
                return {"status": "safety_blocked", "data": None}

            raw_content = (
                content.strip("`").replace("json\n", "", 1).strip()
                if content.startswith("```")
                else content
            )
            return {
                "status": "success",
                "data": json.loads(raw_content),
                "model": model,
            }
        except Exception as e:
            print(f"  [!] Eval failed for {identifier}: {e}")
            return {"status": "failed", "data": None}


def extract_milestones(records: list) -> list:
    """Extracts exactly 4 milestones from a list of GCG steps."""
    if not records:
        return []
    n = len(records)
    indices = [0, max(0, (n - 1) // 3), max(0, 2 * (n - 1) // 3), n - 1]
    return [records[i] for i in indices]


async def process_gcg_logs(
    jsonl_path: str, output_path: str, max_concurrency: int = 10
):
    client = AsyncOpenAI(
        api_key=os.getenv("GEMINI_API_KEY", "missing_key"),
        base_url="[https://generativelanguage.googleapis.com/v1beta/openai/](https://generativelanguage.googleapis.com/v1beta/openai/)",
    )
    semaphore = asyncio.Semaphore(max_concurrency)

    # 1. Group records by Sequence Length and Init Type
    batches = {}
    with open(jsonl_path, "r") as f:
        for line in f:
            rec = json.loads(line)
            # Grouping key: "Length 32 - Random Start"
            key = f"L{rec['sequence_length']}_{rec['init_type']}"
            if key not in batches:
                batches[key] = []
            batches[key].append(rec)

    final_report = {}

    # 2. Process each batch
    for batch_key, records in batches.items():
        print(f"\n[*] Analyzing Trajectory: {batch_key} ({len(records)} steps)")

        # Get t=0, 33, 66, 100
        milestones = extract_milestones(records)
        strings = [m["decoded_string"] for m in milestones]

        # Phase A: Concurrently analyze the 4 milestones
        print(f"  [-] Running Phase A (Milestone Analysis)...")
        phase_a_tasks = [
            safe_eval(
                client,
                PHASE_A_PROMPT.format(prompt_text=s),
                semaphore,
                f"{batch_key}_M{i}",
            )
            for i, s in enumerate(strings)
        ]
        phase_a_results = await asyncio.gather(*phase_a_tasks)

        # Phase B: Independent Trajectory Analysis
        print(f"  [-] Running Phase B (Trajectory Convergence)...")
        phase_b_prompt = PHASE_B_PROMPT.format(
            string_0=strings[0],
            string_1=strings[1],
            string_2=strings[2],
            string_3=strings[3],
        )
        phase_b_result = await safe_eval(
            client, phase_b_prompt, semaphore, f"{batch_key}_Trajectory"
        )

        # Compile
        final_report[batch_key] = {
            "milestone_strings": strings,
            "phase_a_milestones": [res.get("data") for res in phase_a_results],
            "phase_b_trajectory": phase_b_result.get("data"),
        }

    # 3. Save Report
    with open(output_path, "w") as f:
        json.dump(final_report, f, indent=2)
    print(f"\n[+] Analysis complete. Report saved to {output_path}")


if __name__ == "__main__":
    # Point this to your generated JSONL log
    INPUT_LOG = "gcg_trigger_search_latest.jsonl"
    OUTPUT_REPORT = "gcg_trajectory_analysis.json"

    asyncio.run(process_gcg_logs(INPUT_LOG, OUTPUT_REPORT, max_concurrency=10))
